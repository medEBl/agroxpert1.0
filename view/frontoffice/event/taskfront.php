// JavaScript to toggle the full description when the "See More" button is clicked
document.querySelectorAll('.see-more-btn').forEach(button => {
    button.addEventListener('click', function() {
        const eventId = this.getAttribute('data-id');
        const fullDescriptionDiv = document.getElementById('full-description-' + eventId);
        
        if (fullDescriptionDiv.style.display === 'none' || fullDescriptionDiv.style.display === '') {
            fullDescriptionDiv.style.display = 'block';
            this.textContent = 'See Less'; // Change button text
        } else {
            fullDescriptionDiv.style.display = 'none';
            this.textContent = 'See More'; // Reset button text
        }
    });
});
